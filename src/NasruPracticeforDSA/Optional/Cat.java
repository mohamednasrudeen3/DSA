package NasruPracticeforDSA.Optional;

public class Cat {
    String name;
    int age;


    Cat(String name){
        this.name=name;

    }
    Cat(String name, int Age){
        this.name=name;
        this.age= Age;
    }

    public int getAge(){
        return age;
    }
    public String getName(){
        return getName();
    }



}
